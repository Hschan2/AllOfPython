## 어휘 기반(Lexcon-based) 감성 분석

## Manual
# 모든 단어에 대한 감성 사전을 수동으로 구축
# 감성 사전 - 문서의 각 단어가 가지는 긍정/부정의 정도를 -1(부정)부터 1(긍정) 사이의 점수로 레이블링
# 감성이 들어갈 수 있는 품사, 명사와 형용사 동사 키워드를 추출한 뒤 긍정/부정 레이블링을 진행
# 연관어 분석을 통해 어떤 단어가 해당하는 단어들과 같이 사용되었는지 확인 가능

# 한 번 수행하고 나면 적용하기 쉽다는 장점이 있다.
# 도메인에 따라 사용하는 어휘가 달라지고 긍정/부정 점수도 달라질 수 있기 때문에 모든 도메인에 적용할 수 있는 감성 사전을 구축하는 것은 매우 어렵다.

## Dictionary-based
# 사전 기반 접근법
# 기존에 잘 구축되어 있는 외부 사전을 차용한다.
# 외부에서 가져오기 때문에 수동으로 구축하지 않아도 가능하며 분석에 적용하기 쉽다는 장점
# 도메인 확장성이 없다는 단점

# 예. 졸리다 - 영화에서 부정적인 의미일 수 있으나, 침대에서는 긍정적인 의미일 수 있다.

## Corpus-based
# 말뭉치에 맞는 적절한 감성 어휘를 재구축하는 말뭉치 기반 접근 방법
# 이전 방법들의 단점인 도메인 의존성을 극복할 수 있지만 좋은 사전을 구축하기 위해서 많은 데이터(큰 말뭉치)를 필요로 한다는 조건
# 말뭉치를 분석하는 경우에 해당 단어가 사용되었을 때 문장의 긍정/부정을 t-Test를 통해서 판단

