## 감성(감정) 분석 기초

# 감성(감정) 분석
# 텍스트를 분석하여 그 뒤에 감정을 파악할 수 있는 기술
# 기계 학습과 자연어 처리를 결합

# 텍스트 뒤에 있는 감정이 긍정적 혹은 부정적 아니면 중립인지 파악 가능
# 중요한 비즈니스 응용 프로그램을 가진 인공 지능의 강력한 기술

# 예를 들어, 고객 피드백을 분석할 수 있으며 Twitter, Facebook 등 다양한 서비스를 통해 피드백을 수집하고 해당 텍스트 스니펫에서 감정 분석 알고리즘을 실행하여 제품에 대한 고객의 태도 파악 가능

# 감정 분석에 대한 자동 접근 방식은 클러스터링과 같은 기계 학습 모델에 의존
# 긴 텍스트 조각이 분류기에 공급되고 결과는 긍정적, 중립, 부정적으로 반환

## 감정 분석의 자동화 시스템
# 1. 규칙 기반 시스템
# 규칙 기반 접근 방식은 사용자 지정 규칙에 따라 데이터 분류
# 토큰화, 구문 분석, 형태소 분석의 기술이 존재
# 규칙 기반 접근법을 사용자 정의할 수 있으며 더욱 스마트한 규칙을 개발하여 상황에 따라 맞춤형으로 만들 수 있다.
# 일관되고 개선된 결과를 보장하기 위해 이러한 유형의 규칙 기반 모델을 정기적으로 유지해야 한다.

# 2. 하이브리드 시스템
# 감성 분석에서 가장 현대적이고 효율적이며 널리 알려지고 사용되는 접근 방식
# 잘 설계된 시스템은 자동 및 규칙 기반 시스템의 이점을 모두 제공 가능
# 사용자 지정의 유연성과 함께 머신러닝의 장점을 포함
# 모델의 예로 Word2Vec을 기반으로 하는 자동 업데이트 단어 목록
# 단어 목록을 추적하고 비즈니스 요구에 따라 업데이트 가능

